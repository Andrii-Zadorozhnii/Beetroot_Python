def is_prime(n: int) -> bool:
    """
    Проверяет, является ли число простым.

    :param n: Число для проверки
    :return: True, если число простое, иначе False
    """
    if n < 2:
        return False  # Числа меньше 2 не являются простыми

    # Перебираем возможные делители от 2 до √n
    for i in range(2, int(n ** 0.5) + 1):
        if n % i == 0:  # Если число делится без остатка, оно не простое
            return False
    return True  # Если делителей не найдено, число простое


# Создаём пустой список для простых чисел
primes: list[int] = []

# Заполняем список только простыми числами из диапазона 1-100
for num in range(1, 101):
    if is_prime(num):
        primes.append(num)  # Добавляем только простые числа

# Выводим список простых чисел
print(
    "Список простых чисел от 1 до 100:", primes
)
